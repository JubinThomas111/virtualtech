import os
import sys
from github import Github, Auth
from google import genai

def main():
    # 1. Setup environment variables (Provided by GitHub Actions)
    token = os.getenv("GITHUB_TOKEN")
    gemini_key = os.getenv("GEMINI_API_KEY")
    repo_name = os.getenv("GITHUB_REPOSITORY")
    pr_num = os.getenv("PR_NUMBER")
    
    if not all([token, gemini_key, repo_name, pr_num]):
        print("❌ Error: Missing required environment variables (GITHUB_TOKEN, GEMINI_API_KEY, etc).")
        sys.exit(1)

    print(f"🔍 Starting Logic Extraction for PR #{pr_num} in {repo_name}...")

    try:
        # 2. Initialize Clients
        # Using v1beta for compatibility with latest Gemini features
        client = genai.Client(api_key=gemini_key, http_options={'api_version': 'v1beta'})
        gh = Github(auth=Auth.Token(token))
        
        repo = gh.get_repo(repo_name)
        pr = repo.get_pull(int(pr_num))

        # 3. Extract & Truncate Code Diffs (Quota Protection)
        diff_content = ""
        MAX_PATCH_LENGTH = 5000  # Limits characters per file to prevent 429 Quota Errors
        
        for file in pr.get_files():
            # Include standard Broadcom tech stack files
            if file.filename.endswith(('.py', '.java', '.xml', '.gradle', '.js', '.ts', '.sh')):
                if file.patch:
                    # Truncate long patches to save tokens
                    patch = file.patch[:MAX_PATCH_LENGTH]
                    diff_content += f"\n--- File: {file.filename} ---\n{patch}\n"

        if not diff_content:
            print("⚠️ No relevant code changes found. Skipping AI generation.")
            return

        # 4. Generate Documentation with Gemini (Stability Optimized)
        # 1.5-Flash is used here for highest reliability/quota on personal accounts
        model_id = "gemini-1.5-pro" 
        
        prompt = f"""
        Act as a Senior Technical Writer following the Diátaxis documentation framework. 
        Analyze the following code changes and generate a concise 'How-to' guide.
        Focus on practical, goal-oriented steps for a developer audience.
        
        Format:
        ### 🎯 Feature Overview
        (Describe what this change does in 2 clear sentences)
        
        ### 🛠️ Usage / Implementation
        (Provide a clear 'How-to' step or code snippet showing how to use the new logic)
        
        ### 📝 Prerequisites
        (List any specific dependencies, environment variables, or config required)
        
        CODE DIFFS:
        {diff_content}
        """

        print(f"🤖 Consulting {model_id} via Secure AI Pipeline...")
        response = client.models.generate_content(model=model_id, contents=prompt)
        
        # 5. Post to GitHub
        # Clean response text from Gemini
        ai_draft = response.text
        
        comment_header = "## 📘 Automated AI Documentation Draft\n"
        comment_footer = "\n\n---\n*Generated by Broadcom TechDocs AI Pipeline (Gemini 1.5 Flash)*"
        
        full_comment = f"{comment_header}{ai_draft}{comment_footer}"
        
        pr.create_issue_comment(full_comment)
        
        print("🚀 Success! Documentation comment posted to the PR.")

    except Exception as e:
        # Improved error reporting for 429/503 issues
        if "429" in str(e):
            print("❌ Quota Error: Personal account rate limit reached. Wait 60s and retry.")
        elif "503" in str(e):
            print("❌ API Error: Gemini service is temporarily unavailable. Retry in a moment.")
        else:
            print(f"❌ Critical Error: {str(e)}")
        sys.exit(1)

if __name__ == "__main__":
    main()
